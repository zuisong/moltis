// ── Build function ────────────────────────────────────────────────────

function buildLang(lang) {
  let html = readFileSync(TEMPLATE, "utf-8");

  // 1. html lang
  html = html.replace('lang="en"', `lang="${lang.code}"`);

  // 2. og:locale
  html = html.replace('"en_US"', `"${lang.locale}"`);

  // 3. title
  html = html.replace(
    /<title>Moltis — A secure persistent personal agent server in Rust<\/title>/,
    `<title>${lang.title}</title>`
  );

  // 4. meta description (content attribute)
  const enDesc = "A secure persistent personal agent server in Rust. One binary, sandboxed execution, multi-provider LLMs, voice, memory, Telegram, WhatsApp, Discord, Slack, Matrix, Nostr, Teams, and MCP tools. Secure by design, runs on your hardware.";
  html = html.replaceAll(enDesc, lang.metaDesc);

  // 5. og:title / twitter:title
  html = html.replaceAll(
    'content="Moltis — A secure persistent personal agent server in Rust"',
    `content="${lang.title}"`
  );

  // 6. Language toggle: change button text and highlight
  html = html.replace(
    /(<button onclick="this\.nextElementSibling\.classList\.toggle\('hidden'\)"[^>]*>)\s*EN\s*/,
    `$1\n                            ${lang.langBtn}\n                            `
  );
  html = html.replace('title="Change language"', `title="${lang.langTitle}"`);

  // Highlight the correct language in dropdown
  // First reset English highlight to normal
  html = html.replace(
    /onclick="setLang\('en'\)" class="[^"]*font-semibold text-orange-600 dark:text-orange-400[^"]*"/,
    `onclick="setLang('en')" class="w-full text-left px-3 py-1.5 text-xs text-gray-600 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700"`
  );
  // Then set the current language to highlighted
  const highlightClass = "w-full text-left px-3 py-1.5 text-xs font-semibold text-orange-600 dark:text-orange-400 hover:bg-gray-100 dark:hover:bg-gray-700";
  const normalClass = "w-full text-left px-3 py-1.5 text-xs text-gray-600 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700";
  const langBtnRegex = new RegExp(
    `(onclick="setLang\\('${lang.code}'\\)" class=")${normalClass.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}"`,
  );
  html = html.replace(langBtnRegex, `$1${highlightClass}"`);

  // 7. Apply all string replacements (longest first for safety)
  const sorted = [...lang.strings].sort((a, b) => b[0].length - a[0].length);
  for (const [en, translated] of sorted) {
    html = replaceTranslatedString(html, en, translated);
  }

  writeFileSync(lang.file, html, "utf-8");
  console.log(`  ✓ ${lang.file} (${html.split("\n").length} lines)`);
}

// ── Main ──────────────────────────────────────────────────────────────

console.log("Building i18n files from", TEMPLATE, "...\n");

for (const [code, lang] of Object.entries(LANGUAGES)) {
  buildLang(lang);
}

console.log("\nDone.");
