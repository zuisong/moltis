function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function replaceTranslatedString(html, en, translated) {
  if (/^[A-Za-z]+$/.test(en)) {
    return html.replace(new RegExp(`\\b${escapeRegExp(en)}\\b`, "g"), translated);
  }

  return html.replaceAll(en, translated);
}

